#include <iostream>
using namespace std;
int main()
{
    int x;     // declaration
    x = 5;     // initialization
    cout << x;          // Output : 5
    cout << endl;
    cout << x + 5;      // 10
    cout << endl;
    int y = 15;        // declaration & initialization
    cout << x + y;      // 20
    cout << endl;
    cout << x - y;      // -10
    cout << endl;
    cout << x * y;      // 75 
    cout << endl;
    cout << x / y;      // 0
}